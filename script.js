function checkDate() {
    let present = new Date().getDate();
    let setDate = new Date('August 8, 2024').getDate();

    if (present == setDate) {
        alert('Happy 6 Months!');
    }
    else {
        var timeBox = document.getElementById('time-box');
        timeBox.innerHTML = 'Almost 6 months!';
    }



}

window.onload(checkDate());

